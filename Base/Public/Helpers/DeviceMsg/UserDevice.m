-(id)init{

    NSString * strUUID = (NSString *)[KeyChainStore load:@"com.company.app.usernamepassword"];
    
    //首次执行该方法时,uuid为空
    if ([strUUID isEqualToString:@""] || !strUUID)
    {
        //生成一个uuid的方法
        CFUUIDRef uuidRef = CFUUIDCreate(kCFAllocatorDefault);
        
        strUUID = (NSString *)CFBridgingRelease(CFUUIDCreateString (kCFAllocatorDefault,uuidRef));
        
        //将该uuid保存到keychain
        [KeyChainStore save:@"com.company.app.usernamepassword" data:strUUID];

    }
    
    self.deviceWiFi = [self getDeviceWiFi];
    self.deviceMessage = [self getDeviceMessage];
    self.isJailBreak = [CommonHelper_iOS isJailBreak];
    
    return self;
}

-(NSDictionary *)getDeviceMessage{
    
    //从钥匙串读取UUID
    NSString *deviceId = [KeyChainStore load:@"com.company.app.usernamepassword"];

    UIDevice *device = [UIDevice currentDevice];
    
    NSString *isRootOrJailbreak;
    if([CommonHelper_iOS isJailBreak])
    {
        isRootOrJailbreak =@"Y";
    }
    else{
        isRootOrJailbreak =@"N";
    }
    
    NSDictionary *deviceDic = @{@"deviceId":deviceId,@"deviceType":@"IOS",@"isRootOrJailbreak":isRootOrJailbreak,@"deviceOS":device.systemVersion,@"deviceBrand":@"APPLE",@"deviceModel":[self getPhoneModel],@"network":[self getCarrier]};
    
//    DebugLog(@"%@",deviceDic);
    return deviceDic;
}

//用来辨别设备所使用网络的运营商
- (NSString*)getCarrier
{
    NSString *ret = [[NSString alloc]init];
    CTTelephonyNetworkInfo *info = [[CTTelephonyNetworkInfo alloc] init];
    CTCarrier *carrier = [info subscriberCellularProvider];
    if (carrier == nil) {
        return @"0";
    }
    NSString *code = [carrier mobileNetworkCode];
    if ([code isEqual: @""]) {
        return @"0";
    }
    if ([code isEqualToString:@"00"] || [code isEqualToString:@"02"] || [code isEqualToString:@"07"]) {
        ret = @"CM";
    }
    if ([code isEqualToString:@"01"]|| [code isEqualToString:@"06"] ) {
        ret = @"CU";
    }
    if ([code isEqualToString:@"03"]|| [code isEqualToString:@"05"] ) {
        ret = @"CT";
    }
    return ret;
}

